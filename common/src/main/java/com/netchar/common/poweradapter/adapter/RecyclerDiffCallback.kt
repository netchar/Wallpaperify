package com.netchar.common.poweradapter.adapter

import androidx.recyclerview.widget.DiffUtil
import com.netchar.common.poweradapter.item.IRecyclerItem

class RecyclerDiffCallback : DiffUtil.Callback() {

    private lateinit var oldList: List<IRecyclerItem>
    private lateinit var newList: List<IRecyclerItem>

    fun update(oldList: List<IRecyclerItem>, newList: List<IRecyclerItem>) {
        this.oldList = oldList
        this.newList = newList
    }

    override fun getOldListSize(): Int {
        return oldList.size
    }

    override fun getNewListSize(): Int {
        return newList.size
    }

    override fun areItemsTheSame(oldItemPosition: Int, newItemPosition: Int): Boolean {
        return oldList[oldItemPosition].getId() == newList[newItemPosition].getId()
    }

    override fun areContentsTheSame(oldItemPosition: Int, newItemPosition: Int): Boolean {
        return oldList[oldItemPosition] == newList[newItemPosition]
    }
}
